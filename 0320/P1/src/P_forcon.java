public class P_forcon {
    public static void main(String[] args) {
        // 배열 생성
        int[] arr = new int[3];

        // 배열 자체의 주소 출력
        System.out.println("배열의 주소: " + arr);

        // 배열 요소(첫 번째 요소)의 주소 출력
        System.out.println("첫 번째 요소의 주소: " + arr[0]);

        // 배열 요소(두 번째 요소)의 주소 출력
        System.out.println("두 번째 요소의 주소: " + arr[1]);
    }
}
