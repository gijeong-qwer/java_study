public class P5 {
    public static void main(String[] args) {
        // 변수....가장 중요.....
        // 변수(메모리) 선언 문법:
        // 타입 변수명;
        // 기본 (primitive)타입: 정수(byte, short, int, long), 실수(float, double), 문자(char), 논리(boolean)
        byte a; // a 라는 이름을 가진 변수를 선언한다. 메모리를 생성한다.
        a = 10;
        b = 20; 
        int b = 20; // 변수 선언과 값의 초기화를 일반적으로는 같이한다.
        b = 30;
        int b = 40; // 중복 선언 불가!!

        int c = 40; //40을 만들고 싶으면 문법 선언 다른 변수
        
    }
}
